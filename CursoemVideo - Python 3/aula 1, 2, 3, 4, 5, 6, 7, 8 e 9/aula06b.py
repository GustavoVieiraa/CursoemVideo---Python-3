c = input("Digite qualquer coisa: ")
print('Só tem letras maiúsculas? :', c.isupper())
print('É Alfabetico? :', c.isalpha())
print('É Alfabetico ou/e  númerico? :' ,c.isalnum())
print('É um número? :', c.isnumeric())
print('Só contém espaços? :', c.isspace())
print('Só tem letras minusculas? :', c.islower())




